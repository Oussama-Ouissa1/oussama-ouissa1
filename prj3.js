function open1(){
  var op = document.getElementById('mdl')
  op.style.display='block' 
  var x = document.getElementById ('bd')
  x.style.opacity="0.1";
  x.style.backgroundColor="black"
}

function close1(){
  var op = document.getElementById('mdl')
  op.style.display='none' 
}

